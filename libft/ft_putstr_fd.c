#include "libft.h"

size_t	ft_putstr_fd(char *s, int fd)
{
	size_t	size;

	size = 0;
	if (!s)
		return (write(1, "(null)", 6));
	while (*s)
		size += write(fd, s++, 1);
	return (size);
}
